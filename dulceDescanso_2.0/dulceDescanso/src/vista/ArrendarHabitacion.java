    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked

        String rut, nombre, apellido, telefono, correoAux, setGenero, dia, mes, acno, fechaStr, dia2, mes2, acno2, fechaStr2, num;
        int auxMes;

        Registro r = new Registro();
        Date checkin;
        Date chekout;
        String b;
        int numHabPas;

        rut = txtRutPas.getText();
        if (rut.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Rut", "Validación", JOptionPane.WARNING_MESSAGE);
            txtRutPas.requestFocus();
            return;
        }

        nombre = txtNombrePas.getText();
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Su nombre", "Validación", JOptionPane.WARNING_MESSAGE);
            txtRutPas.requestFocus();
            return;
        }

        apellido = txtApellidoPas.getText();
        if (apellido.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Su Apellido", "Validación", JOptionPane.WARNING_MESSAGE);
            txtRutPas.requestFocus();
            return;
        }

        telefono = (txtTelefono.getText());
        if (rut.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Rut", "Validación", JOptionPane.WARNING_MESSAGE);
            txtRutPas.requestFocus();
            return;
        }

        correoAux = (correo.getText());
        if (rut.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Rut", "Validación", JOptionPane.WARNING_MESSAGE);
            txtRutPas.requestFocus();
            return;
        }

        setGenero = (genero.getText());
        if (rut.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Rut", "Validación", JOptionPane.WARNING_MESSAGE);
            txtRutPas.requestFocus();
            return;
        }

        dia = txtDia.getText();
        mes = txtMes.getText();
        acno = txtAcno.getText();
        if (dia.isEmpty() || mes.isEmpty() || acno.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese fecha", "Validación", JOptionPane.WARNING_MESSAGE);
            txtDia.requestFocus();
            return;
        }

        dia2 = txtDia2.getText();
        mes2 = txtMes2.getText();
        acno2 = txtAcno2.getText();
        if (dia2.isEmpty() || mes2.isEmpty() || acno2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese fecha", "Validación", JOptionPane.WARNING_MESSAGE);
            txtDia.requestFocus();
            return;
        }

        fechaStr = acno + "/" + mes + "/" + dia;
        SimpleDateFormat formatter1 = new SimpleDateFormat("yyyy/MM/dd");
        try {
            checkin = formatter1.parse(fechaStr);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese fecha en el formato dd/mm/yyyy", "Validación", JOptionPane.WARNING_MESSAGE);
            txtDia.requestFocus();
            return;
        }
        fechaStr2 = acno2 + "/" + mes2 + "/" + dia2;
        SimpleDateFormat formatter2 = new SimpleDateFormat("yyyy/MM/dd");
        try {
            chekout = formatter2.parse(fechaStr2);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese fecha en el formato dd/mm/yyyy", "Validación", JOptionPane.WARNING_MESSAGE);
            txtDia.requestFocus();
            return;
        }
        numHabPas = Integer.parseInt((String) listaHabitaciones.getSelectedItem());
        num = (String) listaHabitaciones.getSelectedItem();
        Pasajero pas = new Pasajero(rut, nombre, apellido, telefono, correoAux, setGenero, numHabPas);

        if (!r.buscarPasajero(pas.getRutPas())) {
            if (r.ingresarPasajero(pas, checkin, chekout, num)) {
                JOptionPane.showMessageDialog(this, "Se agregó la reserva para la habitacion " + numHabPas, "Información", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se agregó la reserva para la habitacion " + numHabPas, "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "El rut: " + pas.getRutPas() + " ya se encuentra registrado", "Información", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarMouseClicked

