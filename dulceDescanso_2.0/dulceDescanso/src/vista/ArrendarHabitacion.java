//Tiempo Fecha
    public void mostrarTiempo() {
        DateTimeFormatter formateador = DateTimeFormatter.ofPattern("HH:mm:ss");
        Runnable runnable = new Runnable() {
            @Override
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(500);
                        labelHora.setText(formateador.format(LocalDateTime.now()));
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
            }
        };
        Thread hilo = new Thread(runnable);
        hilo.start();

        Calendar fecha = new GregorianCalendar();

        String anho = Integer.toString(fecha.get(Calendar.YEAR));
        String mes = Integer.toString((fecha.get(Calendar.MONTH)) + 1);
        String dia = Integer.toString(fecha.get(Calendar.DATE));

        String fechaComp = dia + "/" + mes + "/" + anho;
        labelFecha.setText(fechaComp);

    }

//LLenar Lista
    public void llenarLista() {
        try {
            Conexion conexion1 = new Conexion();
            Connection cnx = conexion1.obtenerConexion();

            String query = "SELECT  codHabitacion  from habitacion where estado='disponible'";
            PreparedStatement stmt = cnx.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                listaHabitaciones.addItem(Integer.toString(rs.getInt(1)));
            }
            rs.close();
            stmt.close();
            cnx.close();
        } catch (SQLException e) {
            System.out.println("Error SQL al agregar Habitación" + e.getMessage());
        } catch (Exception e) {
            System.out.println("Error al agregar Habitación" + e.getMessage());
        }
    }

